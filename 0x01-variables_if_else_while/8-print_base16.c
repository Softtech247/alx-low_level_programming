#include <stdlib.h>
#include <time.h>
#include <stdio.h>
/**
 * main - Entry point
 * Description: 'get the last digit of the number'
 * Return: always 0
 */
int main(void)
{
	int n = 48;
	int g = 97;

	while (n <= 57)
	{
		putchar(n);
		n++;
	}
	while (g <= 102)
	{
		putchar(g);
		g++;
	}
	putchar('\n');
	return (0);
}
